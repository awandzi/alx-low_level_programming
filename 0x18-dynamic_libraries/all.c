#include <stdlib.h>

/*
 * mod - mod of two numbers
 * @a: first int
 * @b: second int
 * Return: mod
 */

int mod(int a, int b)
{
	return (a % b);
}
/*
 * add - add two numbers
 * @a: first int
 * @b: second int
 * Return: addition
 */

int add(int a, int b)
{
	return (a + b);
}
/*
 * mul - multiply of two numbers
 * @a: first int
 * @b: second int
 * Return: multiplication
 */
int mul(int a, int b)
{
	return (a * b);
}
/*
 * sub - subtract two numbers
 * @a: first int
 * @b: second int
 * Return: subtraction
 */

int sub(int a, int b)
{
	return (a - b);
}

/*
 * div - divid of two numbers
 * @a: first int
 * @b: second int
 * Return: division
 */

int div(int a, int b)
{
	return (a / b);
}

